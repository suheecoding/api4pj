DROP TABLE ACCINFO IF EXISTS;
CREATE TABLE ACCINFO ( 
    ACCTNO VARCHAR(10) NOT NULL
  , NAME VARCHAR(50) NOT NULL
  , BRCODE VARCHAR(1) NOT NULL
  , PRIMARY KEY(ACCTNO)
);

DROP TABLE BRANCHINFO IF EXISTS;
CREATE TABLE BRANCHINFO ( 
    BRCODE VARCHAR(1) NOT NULL
  , BRNAME VARCHAR(10) NOT NULL
  , PRIMARY KEY(BRCODE)
);

DROP TABLE TRANHIS IF EXISTS;
CREATE TABLE TRANHIS ( 
    TRNFLDDAT  DATE NOT NULL
  , ACCTNO VARCHAR(10) NOT NULL
  , TRNFLDNBR VARCHAR(2)  NOT NULL
  , TRNFLDAMT NUMBER NOT NULL
  , TRNFLDFEE NUMBER 
  , CANFLDYON VARCHAR(1)  NOT NULL
  , PRIMARY KEY(TRNFLDDAT, ACCTNO, TRNFLDNBR)
);